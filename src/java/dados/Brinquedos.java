package dados;

public class Brinquedos {
    
    String nomecategoria;
    int idcategoria;
    
    String nomebrinquedo;
    int idbrinquedo;
    String preço;
    String marca;
    String descrição;

    public String getNomecategoria() {
        return nomecategoria;
    }

    public void setNomecategoria(String nomecategoria) {
        this.nomecategoria = nomecategoria;
    }

    public int getIdcategoria() {
        return idcategoria;
    }

    public void setIdcategoria(int idcategoria) {
        this.idcategoria = idcategoria;
    }

    public String getNomebrinquedo() {
        return nomebrinquedo;
    }

    public void setNomebrinquedo(String nomebrinquedo) {
        this.nomebrinquedo = nomebrinquedo;
    }

    public int getIdbrinquedo() {
        return idbrinquedo;
    }

    public void setIdbrinquedo(int idbrinquedo) {
        this.idbrinquedo = idbrinquedo;
    }

    public String getPreço() {
        return preço;
    }

    public void setPreço(String preço) {
        this.preço = preço;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getDescrição() {
        return descrição;
    }

    public void setDescrição(String descrição) {
        this.descrição = descrição;
    }
    
    
}
